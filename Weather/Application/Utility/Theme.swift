import UIKit

enum WeatherTheme {
    enum Font {
        static let regular16: UIFont = UIFont.fixedCustomFont(font: .regular, size: 16)
        static let regular12: UIFont = UIFont.fixedCustomFont(font: .regular, size: 12)
        static let regular10: UIFont = UIFont.fixedCustomFont(font: .regular, size: 10)
        static let medium16: UIFont = UIFont.fixedCustomFont(font: .medium, size: 16)
        static let bold36: UIFont = UIFont.fixedCustomFont(font: .bold, size: 36)
    }
}
